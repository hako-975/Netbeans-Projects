    private void kodeMobilTextFieldCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_kodeMobilTextFieldCaretUpdate
        // TODO add your handling code here:
        try {
            Connection c;
            c = koneksi.getConnection();
            Statement s = c.createStatement();
            String sql = "Select * from mobil where kodeMobil='" + this.kodeMobilTextField.getText() + "'";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                this.lblMerkMobil.setText(r.getString("merkMobil"));
                this.lblTahunMobil.setText(r.getString("tahunMobil"));
                this.lblHargaMobil.setText(r.getString("hargaMobil"));
            }
            r.close();
            s.close();

        } catch (SQLException e) {
            System.out.println("Terjadi kesalahan" + e);
        }
    }//GEN-LAST:event_kodeMobilTextFieldCaretUpdate

    private void bayarTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_bayarTextFieldKeyReleased
        // TODO add your handling code here:
        lblHargaMobil.getText();
        lblKembalian.setText(String.valueOf(Integer.parseInt(bayarTextField.getText()) - Integer.parseInt(lblHargaMobil.getText())));
    }//GEN-LAST:event_bayarTextFieldKeyReleased

    private void nikPembeliTextFieldCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_nikPembeliTextFieldCaretUpdate
        // TODO add your handling code here:
        try {
            Connection c;
            c = koneksi.getConnection();
            Statement s = c.createStatement();
            String sql = "Select * from pembeli where nikPembeli='" + this.nikPembeliTextField.getText() + "'";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                this.lblNamaPembeli.setText(r.getString("namaPembeli"));
            }
            r.close();
            s.close();

        } catch (SQLException e) {
            System.out.println("Terjadi kesalahan" + e);
        }
    }//GEN-LAST:event_nikPembeliTextFieldCaretUpdate


