    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        koneksi.InsertKontak(txtNama.getText(), txtNoTelepon.getText(), txtEmail.getText(), txtAlamat.getText(), txtPerusahaan.getText());
        try {
            refreshTable();
        } catch (SQLException ex) {
            Logger.getLogger(DaftarKontakHp.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtNama.setText("");
        txtNoTelepon.setText("");
        txtEmail.setText("");
        txtAlamat.setText("");
        txtPerusahaan.setText("");
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int row = tblKontakHp.getSelectedRow();
        txtNama.setText((String) tblKontakHp.getValueAt(row, 0));
        txtNoTelepon.setText((String) tblKontakHp.getValueAt(row, 1));
        txtEmail.setText((String) tblKontakHp.getValueAt(row, 2));
        txtAlamat.setText((String) tblKontakHp.getValueAt(row, 3));
        txtPerusahaan.setText((String) tblKontakHp.getValueAt(row, 4));

        btnUpdate.setVisible(true);
        btnSimpan.setVisible(false);
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tblKontakHp.getSelectedRow();
        koneksi.UpdateKontak(
                txtNama.getText(),
                txtNoTelepon.getText(),
                txtEmail.getText(),
                txtAlamat.getText(),
                txtPerusahaan.getText(),
                Integer.parseInt((String) tblKontakHp.getValueAt(row, 5)));
        
        txtNama.setText("");
        txtNoTelepon.setText("");
        txtEmail.setText("");
        txtAlamat.setText("");
        txtPerusahaan.setText("");
        
        try {
            refreshTable();
        } catch (SQLException ex) {
            Logger.getLogger(DaftarKontakHp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblKontakHp.getSelectedRow();
        koneksi.DeleteKontak(Integer.parseInt((String) tblKontakHp.getValueAt(row, 5)));
        try {
            refreshTable();
        } catch (SQLException ex) {
            Logger.getLogger(DaftarKontakHp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        try {
            // TODO add your handling code here:
            File fileName = new File("src/report/reportKontakHp.jasper");
            JasperPrint jp;
            jp = JasperFillManager.fillReport(fileName.getPath(), null, koneksi.con);
            JasperViewer.viewReport(jp, false);
        } catch (JRException ex) {
            Logger.getLogger(DaftarKontakHp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCetakActionPerformed

