    private void refreshTable() throws SQLException {
        DefaultTableModel tblModel = (DefaultTableModel) tblKontakHp.getModel();
        tblModel.setRowCount(0); // mengosongkan table setiap ada perubahaan
        ResultSet dataKontak = koneksi.GetDataKontak();

        while (dataKontak.next()) {
            tblModel.addRow(new Object[]{
                dataKontak.getString("nama_lengkap"),
                dataKontak.getString("no_telepon"),
                dataKontak.getString("email"),
                dataKontak.getString("alamat"),
                dataKontak.getString("perusahaan"),
                dataKontak.getString("id_kontak_hp")
            });
        }
    }

