    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = pesertaTable.getSelectedRow();
        if (selectedRow != -1) {
            String nim = (String) tableModel.getValueAt(selectedRow, 0);
            String nama = namaField.getText();
            String alamat = alamatField.getText();
            String telp = telpField.getText();
            String alasan = alasanField.getText();

            Peserta peserta = new Peserta(nim, nama, alamat, telp, alasan);
            pesertaController.updatePeserta(peserta);
            refreshTable();
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diubah.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = pesertaTable.getSelectedRow();
        if (selectedRow != -1) {
            String nim = (String) tableModel.getValueAt(selectedRow, 0);
            pesertaController.deletePeserta(nim);
            refreshTable();
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String keyword = JOptionPane.showInputDialog(this, "Masukan kata kunci yang ingin dicari:");
        List<Peserta> searchResults = pesertaController.searchPeserta(keyword);
        tableModel.setRowCount(0);

        for (Peserta peserta : searchResults) {
            Object[] rowData = {peserta.getNim(), peserta.getNama(), peserta.getAlamat(), peserta.getTelp(), peserta.getAlasan()};
            tableModel.addRow(rowData);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String nim = nimField.getText();
        String nama = namaField.getText();
        String alamat = alamatField.getText();
        String telp = telpField.getText();
        String alasan = alasanField.getText();
        
        if (nim.isEmpty() || nama.isEmpty() || alamat.isEmpty() || telp.isEmpty() || alasan.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua kolom wajib diisi.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Peserta peserta = new Peserta(nim, nama, alamat, telp, alasan);
            pesertaController.addPeserta(peserta);
        }
        
        refreshTable();
        clearFields();
    }//GEN-LAST:event_btnSaveActionPerformed

