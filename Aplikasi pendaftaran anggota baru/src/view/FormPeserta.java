    private void pesertaTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pesertaTableMouseClicked
        // TODO add your handling code here:
        int selectedRow = pesertaTable.getSelectedRow();
        if (selectedRow != -1) { // Make sure a row is selected
            String nim = pesertaTable.getValueAt(selectedRow, 0).toString();
            String nama = pesertaTable.getValueAt(selectedRow, 1).toString();
            String alamat = pesertaTable.getValueAt(selectedRow, 2).toString();
            String telp = pesertaTable.getValueAt(selectedRow, 3).toString();
            String alasan = pesertaTable.getValueAt(selectedRow, 4).toString();

            nimField.setText(nim);
            namaField.setText(nama);
            alamatField.setText(alamat);
            telpField.setText(telp);
            alasanField.setText(alasan);
        }
    }//GEN-LAST:event_pesertaTableMouseClicked

