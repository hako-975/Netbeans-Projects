    public void refreshTable() throws SQLException {
        DefaultTableModel tblModel = (DefaultTableModel) tbl_barang.getModel();
        // mengosongkan table
        tblModel.setRowCount(0);
        ResultSet dataBarang = db.GetDataBarang();
        
        while (dataBarang.next()) {
            tblModel.addRow(new Object[] {
                dataBarang.getString("id_barang"),
                dataBarang.getString("nama_barang"),
                dataBarang.getString("harga_beli"),
                dataBarang.getString("harga_jual"),
                dataBarang.getString("stok"),
                dataBarang.getString("keterangan")
            });
        }
    }

