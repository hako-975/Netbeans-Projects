    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        db.InsertBarang(tf_nama_barang.getText(), 
                Integer.parseInt(tf_harga_beli.getText()), 
                Integer.parseInt(tf_harga_jual.getText()), 
                Integer.parseInt(tf_stok.getText()), 
                tf_keterangan.getText());
        
        try {
            refreshTable();
        } catch (SQLException ex) {
            Logger.getLogger(inventori_barang.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        tf_nama_barang.setText("");
        tf_harga_beli.setText("");
        tf_harga_jual.setText("");
        tf_stok.setText("");
        tf_keterangan.setText("");
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        int row = tbl_barang.getSelectedRow();
        tf_nama_barang.setText((String) tbl_barang.getValueAt(row, 1));
        tf_harga_beli.setText((String) tbl_barang.getValueAt(row, 2));
        tf_harga_jual.setText((String) tbl_barang.getValueAt(row, 3));
        tf_stok.setText((String) tbl_barang.getValueAt(row, 4));
        tf_keterangan.setText((String) tbl_barang.getValueAt(row, 5));
        
        btn_update.setVisible(true);
        btn_simpan.setVisible(false);
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        int row = tbl_barang.getSelectedRow();
        db.UpdateBarang(Integer.parseInt((String) tbl_barang.getValueAt(row, 0)), 
                tf_nama_barang.getText(), 
                Integer.parseInt(tf_harga_beli.getText()), 
                Integer.parseInt(tf_harga_jual.getText()), 
                Integer.parseInt(tf_stok.getText()), 
                tf_keterangan.getText());
        
        try {
            refreshTable();
        } catch (SQLException ex) {
            Logger.getLogger(inventori_barang.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        tf_nama_barang.setText("");
        tf_harga_beli.setText("");
        tf_harga_jual.setText("");
        tf_stok.setText("");
        tf_keterangan.setText("");
        
        btn_simpan.setVisible(true);
        btn_update.setVisible(false);
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        int row = tbl_barang.getSelectedRow();
        db.DeleteBarang(Integer.parseInt((String) tbl_barang.getValueAt(row, 0)));
        
        try {
            refreshTable();
        } catch (SQLException ex) {
            Logger.getLogger(inventori_barang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

