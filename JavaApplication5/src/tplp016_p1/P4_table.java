    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblmodel = (DefaultTableModel) tbl_mahasiswa.getModel();

        P2_data dtMhs = new P2_data();

        dtMhs.setN_uts(Double.parseDouble(tf_uts.getText()));
        dtMhs.setN_uas(Double.parseDouble(tf_uas.getText()));

        String dataMhs[] = {
            tf_nim.getText(),
            tf_nama.getText(),
            tf_uts.getText(),
            tf_uas.getText(),
            Double.toString(dtMhs.hitungNilaiAkhir()),
            String.valueOf(dtMhs.hitungGrade())
        };

        tblmodel.addRow(dataMhs);

        tf_nim.setText("");
        tf_nama.setText("");
        tf_uts.setText("");
        tf_uas.setText("");

    }//GEN-LAST:event_btn_simpanActionPerformed

