    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblmodel = (DefaultTableModel) tbl_mahasiswa.getModel();
        int row = tbl_mahasiswa.getSelectedRow();
        tblmodel.removeRow(row);
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblmodel = (DefaultTableModel) tbl_mahasiswa.getModel();
        int row = tbl_mahasiswa.getSelectedRow();
        tbl_mahasiswa.getValueAt(row, 0);

        tf_nim.setText(tbl_mahasiswa.getValueAt(row, 0).toString());
        tf_nama.setText(tbl_mahasiswa.getValueAt(row, 1).toString());
        tf_uts.setText(tbl_mahasiswa.getValueAt(row, 2).toString());
        tf_uas.setText(tbl_mahasiswa.getValueAt(row, 3).toString());

        btn_update.setVisible(true);
        btn_simpan.setVisible(false);
    }//GEN-LAST:event_btn_ubahActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblmodel = (DefaultTableModel) tbl_mahasiswa.getModel();

        P2_data dtMhs = new P2_data();

        dtMhs.setN_uts(Double.parseDouble(tf_uts.getText()));
        dtMhs.setN_uas(Double.parseDouble(tf_uas.getText()));

        int row = tbl_mahasiswa.getSelectedRow();
        tbl_mahasiswa.setValueAt(tf_nim.getText(), row, 0);
        tbl_mahasiswa.setValueAt(tf_nama.getText(), row, 1);
        tbl_mahasiswa.setValueAt(tf_uts.getText(), row, 2);
        tbl_mahasiswa.setValueAt(tf_uas.getText(), row, 3);
        tbl_mahasiswa.setValueAt(Double.toString(dtMhs.hitungNilaiAkhir()), row, 4);
        tbl_mahasiswa.setValueAt(dtMhs.hitungGrade(), row, 5);

        tf_nim.setText("");
        tf_nama.setText("");
        tf_uts.setText("");
        tf_uas.setText("");

        btn_simpan.setVisible(true);
        btn_update.setVisible(false);

    }//GEN-LAST:event_btn_updateActionPerformed

