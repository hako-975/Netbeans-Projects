    public void refreshTable() throws SQLException {
        DefaultTableModel tblModel = (DefaultTableModel) tbl_mhs.getModel();
        tblModel.setRowCount(0);
        ResultSet dataMhs = db.getDataMahasiswa();
        while (dataMhs.next()) {
            tblModel.addRow(
                    new Object[]{
                        dataMhs.getString("nim"),
                        dataMhs.getString("nama_mahasiswa"),
                        dataMhs.getString("n_uts"),
                        dataMhs.getString("n_uas"),
                        dataMhs.getString("n_akhir"),
                        dataMhs.getString("grade")
                    }
            );
        }
    }

