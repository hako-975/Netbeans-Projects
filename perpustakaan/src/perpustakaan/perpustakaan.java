    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tglPinjam = sdf.format(tf_pinjam.getDate());
        String tglPengembalian = sdf.format(tf_pengembalian.getDate());
        
        modelPer.insertPer(tf_nama.getText(), tglPinjam, tglPengembalian, tf_keterangan.getText());
        
        refreshTable();
        tf_nama.setText("");
        tf_pinjam.setDate(null);
        tf_pengembalian.setDate(null);
        tf_keterangan.setText("");
        
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tglPinjam = sdf.format(tf_pinjam.getDate());
        String tglPengembalian = sdf.format(tf_pengembalian.getDate());
        
        int row = tf_table.getSelectedRow();
        modelPer.updatePer(tf_nama.getText(), tglPinjam, tglPengembalian, tf_keterangan.getText(), Integer.parseInt((String) tf_table.getValueAt(row, 0)));
        
        refreshTable();
        tf_nama.setText("");
        tf_pinjam.setDate(null);
        tf_pengembalian.setDate(null);
        tf_keterangan.setText("");
        
        btn_simpan.setVisible(true);
        btn_edit.setVisible(false);
        btn_hapus.setVisible(false);
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        int row = tf_table.getSelectedRow();
        modelPer.hapusPer(Integer.parseInt((String) tf_table.getValueAt(row, 0)));
        refreshTable();
        
        JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
        
        tf_nama.setText("");
        tf_pinjam.setDate(null);
        tf_pengembalian.setDate(null);
        tf_keterangan.setText("");
        
        btn_simpan.setVisible(true);
        btn_edit.setVisible(false);
        btn_hapus.setVisible(false);
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void tf_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tf_tableMouseClicked
        // TODO add your handling code here:
        int row = tf_table.getSelectedRow();
        Date tglPinjam = null;
        
        try {
            tglPinjam = new SimpleDateFormat("yyyy-MM-dd").parse((String) tf_table.getValueAt(row, 2));
        } catch (ParseException ex) {
            Logger.getLogger(perpustakaan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Date tglPengembalian = null;
        try {
            tglPengembalian = new SimpleDateFormat("yyyy-MM-dd").parse((String) tf_table.getValueAt(row, 3));
        } catch (ParseException ex) {
            Logger.getLogger(perpustakaan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        tf_nama.setText(tf_table.getValueAt(row, 1).toString());
        tf_pinjam.setDate(tglPinjam);
        tf_pengembalian.setDate(tglPengembalian);
        tf_keterangan.setText(tf_table.getValueAt(row, 4).toString());
        
        btn_edit.setVisible(true);
        btn_hapus.setVisible(true);
        btn_simpan.setVisible(false);
    }//GEN-LAST:event_tf_tableMouseClicked

