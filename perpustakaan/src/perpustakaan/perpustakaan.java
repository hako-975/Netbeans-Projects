    public void refreshTable() {
        int jumlahRow = tblPer.getRowCount();
        for (int i = jumlahRow - 1; i >= 0; i--) {
            tblPer.removeRow(i);
        }
        
        ResultSet data = modelPer.getAllPer();
        try {
            while(data.next()) {
                tblPer.addRow(new Object[] {
                    data.getString("no"),
                    data.getString("nama"),
                    data.getString("tanggal_pinjam"),
                    data.getString("tanggal_pengembalian"),
                    data.getString("keterangan")
                });
            }
        } catch(SQLException ex) {
            Logger.getLogger(perpustakaan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
