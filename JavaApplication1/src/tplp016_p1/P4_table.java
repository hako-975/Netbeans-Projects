    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        // mengambil object table
        DefaultTableModel tblModel = (DefaultTableModel) tbl_mhs.getModel();

        P2_data dtMhs = new P2_data();

        dtMhs.setN_uts(Double.parseDouble(tf_uts.getText()));
        dtMhs.setN_uas(Double.parseDouble(tf_uas.getText()));

        // mengambil data
        String dataMhs[] = {
            tf_nim.getText(),
            tf_nama.getText(),
            tf_uts.getText(),
            tf_uas.getText(),
            Double.toString(dtMhs.hitungNilaiAkhir()),
            dtMhs.hitungGrade()
        };

        // menyimpan data ke table
        tblModel.addRow(dataMhs);

        // mengosongkan data input
        tf_nim.setText("");
        tf_nama.setText("");
        tf_uts.setText("");
        tf_uas.setText("");
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        // mengambil object table
        DefaultTableModel tblModel = (DefaultTableModel) tbl_mhs.getModel();

        // mendapatkan row yang di klik
        int row = tbl_mhs.getSelectedRow();

        // menghapus row berdasarkan row
        tblModel.removeRow(row);
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        // mengambil object table
        DefaultTableModel tblModel = (DefaultTableModel) tbl_mhs.getModel();

        // mendapatkan row yang di klik
        int row = tbl_mhs.getSelectedRow();

        // mengambil dan mengganti data input
        tf_nim.setText(tbl_mhs.getValueAt(row, 0).toString());
        tf_nama.setText(tbl_mhs.getValueAt(row, 1).toString());
        tf_uts.setText(tbl_mhs.getValueAt(row, 2).toString());
        tf_uas.setText(tbl_mhs.getValueAt(row, 3).toString());

        // menampilkan button update
        btn_update.setVisible(true);

        // menghilangkan button simpan
        btn_simpan.setVisible(false);
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        // mengambil object table
        DefaultTableModel tblModel = (DefaultTableModel) tbl_mhs.getModel();

        // mengambil data dari class p2
        P2_data dtMhs = new P2_data();
        // set nilai method 
        dtMhs.setN_uts(Double.parseDouble(tf_uts.getText()));
        dtMhs.setN_uas(Double.parseDouble(tf_uas.getText()));

        // mendapatkan row yang di klik
        int row = tbl_mhs.getSelectedRow();

        tbl_mhs.setValueAt(tf_nim.getText(), row, 0);
        tbl_mhs.setValueAt(tf_nama.getText(), row, 1);
        tbl_mhs.setValueAt(tf_uts.getText(), row, 2);
        tbl_mhs.setValueAt(tf_uas.getText(), row, 3);
        tbl_mhs.setValueAt(Double.toString(dtMhs.hitungNilaiAkhir()), row, 4);
        tbl_mhs.setValueAt(dtMhs.hitungGrade(), row, 5);

        // mengosongkan data input
        tf_nim.setText("");
        tf_nama.setText("");
        tf_uts.setText("");
        tf_uas.setText("");

        btn_simpan.setVisible(true);
        btn_update.setVisible(false);
    }//GEN-LAST:event_btn_updateActionPerformed

